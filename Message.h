class Message {
	uint8_t uid;
	uint8_t function_code;
	uint16_t starting_add;
	uint16_t quantity_of_regs;
	static void tokenize(std::string const &str, const char delim,
			std::vector<std::string> &out) {
		std::stringstream ss(str);
		std::string s;
		while (std::getline(ss, s, delim)) {
			out.push_back(s);
		}
	}

public:
	Message(std::string msg);
	virtual ~Message();

	uint8_t getFuncCode() {
		return this->function_code;
	}
	uint16_t getStartAdd() {
		return this->starting_add;
	}
	uint16_t getQuantityOfRegs() {
		return this->quantity_of_regs;
	}

};

